void PID_Control(float current_temperature) {
    float error = - setpoint_temperature + current_temperature;
    float pid_output = Kp * error + Ki * integral + Kd * (error - prev_error);

    // Gi? l?i giá tr? PID trong m?c cho phép
    if (pid_output > 100.0) {
        pid_output = 100.0;
    } else if (pid_output < 0.0) {
        pid_output = 0.0;
    }

    // C?p nh?t giá tr? integral và l?i tru?c dó
    integral += error;
    prev_error = error;

    // Áp d?ng giá tr? PID d? di?u khi?n xung PWM
    __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, (uint32_t)(pid_output * htim2.Init.Period / 100.0));
}

